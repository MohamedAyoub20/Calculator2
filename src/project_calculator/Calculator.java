    private void jbtEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEqualActionPerformed
        if ((jtfResult.getText().startsWith("+")) || (jtfResult.getText().startsWith("-"))
                || (jtfResult.getText().startsWith("*")) || (jtfResult.getText().startsWith("/"))
                || (jtfResult.getText().endsWith("+")) || (jtfResult.getText().endsWith("-"))
                || (jtfResult.getText().endsWith("*")) || (jtfResult.getText().endsWith("/"))) {
            JOptionPane.showMessageDialog(null, "found error in input");
        } else {
            jtfResult.setText(jtfResult.getText() + "="
                    + Double.toString(calc(jtfResult.getText())));
        }
    }//GEN-LAST:event_jbtEqualActionPerformed

