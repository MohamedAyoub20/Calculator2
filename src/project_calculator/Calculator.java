    private void jbtsinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtsinActionPerformed
        if (jtfResult.getText().contains("*") || jtfResult.getText().contains("/")
                || jtfResult.getText().contains("+") || jtfResult.getText().contains("-")) {
            jtfResult.setText("sin(" + jtfResult.getText() + ")"
                    + "=" + Double.toString(Math.sin(Math.toRadians(calc(jtfResult.getText())))));
        } else {
            jtfResult.setText("sin(" + jtfResult.getText() + ")"
                    + "=" + Double.toString(Math.sin(Math.toRadians(Double.parseDouble(jtfResult.getText())))));
        }
    }//GEN-LAST:event_jbtsinActionPerformed

