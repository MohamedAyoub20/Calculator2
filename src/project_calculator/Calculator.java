    private double calc(String text) {
        double result = 0;
        ArrayList<String> number = new ArrayList();
        for (int i = 0; i < text.length(); i++) {
            if ((text.charAt(i) == '+') || (text.charAt(i) == '-')
                    || (text.charAt(i) == '*') || (text.charAt(i) == '/')) {
                number.add(text.substring(0, i));
                number.add(Character.toString(text.charAt(i)));
                text = text.substring(i + 1);
                i = 0;
            }
        }
        number.add(text);

        for (int i = 0; i < number.size(); i++) {
            if ("/".equals(number.get(i))) {
                result = Double.parseDouble(number.get(i - 1)) / Double.parseDouble(number.get(i + 1));
                number.set(i + 1, Double.toString(result));
                number.remove(i);
                number.remove(i - 1);
                i = 0;

            } else if ("*".equals(number.get(i))) {
                result = Double.parseDouble(number.get(i - 1)) * Double.parseDouble(number.get(i + 1));
                number.set(i + 1, Double.toString(result));
                number.remove(i);
                number.remove(i - 1);
                i = 0;
            }

        }

        for (int i = 0; i < number.size(); i++) {
            if ("+".equals(number.get(i))) {
                result = Double.parseDouble(number.get(i - 1)) + Double.parseDouble(number.get(i + 1));
                number.set(i + 1, Double.toString(result));
                number.remove(i);
                number.remove(i - 1);
                i = 0;

            } else if ("-".equals(number.get(i))) {
                result = Double.parseDouble(number.get(i - 1)) - Double.parseDouble(number.get(i + 1));
                number.set(i + 1, Double.toString(result));
                number.remove(i);
                number.remove(i - 1);
                i = 0;
            }

        }
        jbtEqual.setEnabled(false);
        return result;

    }

    private double fac(String number) {
        double num = 1;
        if (number.contains("+") || number.contains("-")
                || number.contains("*") || number.contains("/")) {
            for (int i = 1; i <= calc(number); i++) {
                num = num * i;
            }
            return num;
        } else {
            for (int i = 1; i <= Double.parseDouble(number); i++) {
                num = num * i;
            }
            return num;
        }
    }

